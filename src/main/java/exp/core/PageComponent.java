package exp.core;

import com.microsoft.playwright.Locator;
import com.microsoft.playwright.Page;

/**
 * Интерфейс для компонентов страницы.
 * <p>
 * Определяет методы, которые должны быть реализованы всеми компонентами страницы.
 * Компоненты представляют собой повторно используемые части пользовательского интерфейса,
 * такие как меню навигации, формы, таблицы, модальные окна и т.д.
 * <p>
 * Использование компонентов позволяет:
 * - Структурировать код тестов
 * - Повторно использовать элементы интерфейса
 * - Инкапсулировать логику работы с элементами
 * - Упростить поддержку тестов при изменении интерфейса
 * <p>
 * Реализации этого интерфейса обычно наследуются от BaseComponent.
 */
public interface PageComponent {
    /**
     * Устанавливает объект Page для компонента.
     *
     * @param page объект Playwright Page
     */
    void setPage(Page page);

    /**
     * Устанавливает корневой локатор для компонента.
     * Корневой локатор определяет контейнерный элемент компонента.
     *
     * @param root корневой локатор
     */
    void setRoot(Locator root);

    /**
     * Возвращает объект Page, связанный с компонентом.
     *
     * @return объект Playwright Page
     */
    Page getPage();

    /**
     * Возвращает корневой локатор компонента.
     *
     * @return корневой локатор компонента
     */
    Locator getRoot();
}